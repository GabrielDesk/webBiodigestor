/*
*  BioEnergia
*  O que é 
*  
*  Bioenergia é o nome dado à energia obtida por meio da biomassa (matéria orgânica produzida pelos seres vivos e utilizada para geração energética). Essa energia resultante pode ser utilizada na produção de calor, eletricidade e combustíveis.
*  
*  Porque 
*  
*  -É uma fonte de energia limpa - Emite menos poluentes que as fontes de energia tradicionais, contribuindo para desacelerar o efeito estufa
*  -É Renovável- Permite o reaproveitamento de resíduos que seriam descartados, reduzindo o desperdício.
*  -Tem baixo custo de produção e potencial de geração de energia- além de ser benéfica para o meio ambiente, não exige altos investimentos
*  -Gera biocombustíveis biodegradáveis - Os biocombustíveis poluem menos porque emitem menos compostos do que os combustíveis fósseis na combustão do motor do carro e seu processo é mais limpo.
*  
•Favorece desenvolvimento econômico regional e rural
*  •Melhora processos no setor da agroindústria
*  •Ajuda a melhorar a qualidade do ar
*  •Apresenta custos reduzidos em relação às matrizes tradicionais
*  •Contribui para um menor descarte de resíduos em aterros
*  •Garante uma energia confiável, segura e eficiente
*  •Motiva pesquisas e o uso de tecnologias inovadoras para o meio ambiente
*  
*  
*  Como Produzida 
*  A biomassa é geralmente utilizada como fonte de calor e para a geração de eletricidade a partir de alguns processos. Dessa forma, a energia de biomassa pode ser convertida em calor e depois em outras formas de energia, que podem ser:
*  
*  – Direta – sendo essa a mais utilizada, obtida pela combustão da biomassa na fase sólida;
*  
*  – Indireta – quando são produzidos gases e/ou líquidos combustíveis através da pirólise(tipo de reação quimica produzida pela luz)
*
*/


